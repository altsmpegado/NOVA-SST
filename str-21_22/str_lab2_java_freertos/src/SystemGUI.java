    private void button_packageActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_packageActionPerformed
        if(m.start && !m.blocked_state && !m.interrupted){
            jTextArea1.append("Sending package...\n");
            ThreadGetPart.startGetPart();
        } else jTextArea1.append("Operation currently unavailable...\n");
             
    }//GEN-LAST:event_button_packageActionPerformed

    private void button_conveyorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_conveyorActionPerformed
        
        if(dock1==-1 && dock2==-1)
            jTextArea1.append("Conveyor won't start without dock configuration...\n");
        else{
            button_emergency.setEnabled(true);
            if(!m.start){
                m.start=true;
                m.moveConveyor();
                jTextArea1.append("Conveyor started!\n");
                button_conveyor.setBackground(new java.awt.Color(255, 99, 71));
                button_conveyor.setText("STOP CONVEYOR");
            }
            else{
                m.start=false;
                m.stopConveyor();
                jTextArea1.append("Conveyor stopped!\n");
                button_conveyor.setBackground(new java.awt.Color(102, 255, 102));
                button_conveyor.setText("START CONVEYOR");
            }
        }
    }//GEN-LAST:event_button_conveyorActionPerformed

