    private void button_emergencyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_emergencyActionPerformed
        if(button_emergency.isSelected()){
            jTextArea1.append("SUSPENDING ALL TASKS!\n");
            button_conveyor.setEnabled(false);
            button_package.setEnabled(false);
            button_dockconfig.setEnabled(false);
            
            ThreadStopResume.Stop(); m.stop_state=true;
            
            button_emergency.setText("RESUME OPERATIONS");
        }
        else{
            jTextArea1.append("RESUMING TASKS!\n");
            button_conveyor.setEnabled(true);
            button_package.setEnabled(true);
            button_dockconfig.setEnabled(true);
            
           ThreadStopResume.Resume(); m.stop_state=false;
            
            button_emergency.setText("EMERGENCY STOP");
        }
            
// TODO add your handling code here:
    }//GEN-LAST:event_button_emergencyActionPerformed

